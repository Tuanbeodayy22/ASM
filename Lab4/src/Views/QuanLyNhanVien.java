    public Nhanvien GetformData(){
        String Manv = txtmanv.getText();
        String Tennv = txttennv.getText();
        String GioiTinh = rbnam.isSelected() ? "Nam" : "Nu";
        String ChucVu = (String) cbochucvu.getSelectedItem();
        Nhanvien nv = new Nhanvien(Manv, Tennv, GioiTinh, ChucVu);
        return nv;
        
    }
    public void loadTable( ArrayList<Nhanvien> ds){
        // ArrayList<Nhanvien> ds = nhanvienrepo.serach();
        DefaultTableModel tbl = (DefaultTableModel)tblthongtin.getModel();
        tbl.setRowCount(0);
        for(Nhanvien nv:ds){
            Object[] data = {nv.getMaNV(), nv.getHoten(), nv.getChucVu(), nv.getGioitinh()};
            tbl.addRow(data);
        }
    }
