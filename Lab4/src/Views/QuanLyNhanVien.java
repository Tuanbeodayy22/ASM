    private void tblthongtinMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblthongtinMouseClicked
        int chon = tblthongtin.getSelectedRow();
        String MaNV =(String) tblthongtin.getValueAt(chon, 0);
        String HoTen =(String) tblthongtin.getValueAt(chon, 1);
        String GioiTinh = (String) tblthongtin.getValueAt(chon, 3);
        String ChucVu =(String) tblthongtin.getValueAt(chon, 2);
        txtmanv.setText(MaNV);
        txttennv.setText(HoTen);
            if ("Nam".equals(GioiTinh)) {
            rbnam.setSelected(true);
        } else if ("Nu".equals(GioiTinh)) {
            rbnu.setSelected(true);
    }
        cbochucvu.setSelectedItem(ChucVu);
     
    }//GEN-LAST:event_tblthongtinMouseClicked

